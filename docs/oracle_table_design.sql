-- 3. 거래 테이블 (Transaction Tables)
-- ========================================

-- 3-1. 연차 신청 테이블
--   APPROVAL_USER_ID: 승인/반려 처리한 L4 사용자 ID
--   REJECTION_REASON: 반려 사유 (REJECTED 상태일 때 사용)
CREATE TABLE TB_LEAVE_REQUESTS (
  LEAVE_REQUEST_ID   VARCHAR2(50)   PRIMARY KEY,
  USER_ID            VARCHAR2(50)   NOT NULL,
  START_DATE         DATE           NOT NULL,
  END_DATE           DATE           NOT NULL,
  LEAVE_REASON       VARCHAR2(500)  NOT NULL,
  LEAVE_STATUS_CODE  VARCHAR2(20)   DEFAULT 'PENDING' NOT NULL,
  APPROVAL_USER_ID   VARCHAR2(50),
  APPROVED_AT        TIMESTAMP,
  REJECTION_REASON   VARCHAR2(500),
  CREATED_AT         TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT         TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT FK_LEAVE_REQUESTS_USER          FOREIGN KEY (USER_ID)          REFERENCES TB_USERS(USER_ID),
  CONSTRAINT FK_LEAVE_REQUESTS_APPROVAL_USER FOREIGN KEY (APPROVAL_USER_ID) REFERENCES TB_USERS(USER_ID),
  CONSTRAINT FK_LEAVE_REQUESTS_STATUS        FOREIGN KEY (LEAVE_STATUS_CODE) REFERENCES TB_LEAVE_STATUS_CODE(LEAVE_STATUS_CODE),
  CONSTRAINT CK_LEAVE_DATES CHECK (END_DATE >= START_DATE)
);

CREATE INDEX IDX_LEAVE_REQUESTS_USER          ON TB_LEAVE_REQUESTS(USER_ID);
CREATE INDEX IDX_LEAVE_REQUESTS_STATUS        ON TB_LEAVE_REQUESTS(LEAVE_STATUS_CODE);
CREATE INDEX IDX_LEAVE_REQUESTS_DATES         ON TB_LEAVE_REQUESTS(START_DATE, END_DATE);
CREATE INDEX IDX_LEAVE_REQUESTS_APPROVAL_USER ON TB_LEAVE_REQUESTS(APPROVAL_USER_ID);


-- 3-2. 근태 기록 테이블
--   CHECK_IN_TIME / CHECK_OUT_TIME: HH:mm 형식 문자열
--   ATTENDANCE_STATUS: PRESENT(퇴근 완료) / WORKING(출근 중, 퇴근 전) / ABSENT / LEAVE / HOLIDAY
CREATE TABLE TB_ATTENDANCE_RECORDS (
  ATTENDANCE_RECORD_ID  VARCHAR2(50)   PRIMARY KEY,
  USER_ID               VARCHAR2(50)   NOT NULL,
  ATTENDANCE_DATE       DATE           NOT NULL,
  CHECK_IN_TIME         VARCHAR2(5),
  CHECK_OUT_TIME        VARCHAR2(5),
  ATTENDANCE_STATUS     VARCHAR2(20)   DEFAULT 'PRESENT' NOT NULL,
  REMARKS               VARCHAR2(500),
  CREATED_AT            TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT            TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT FK_ATTENDANCE_USER   FOREIGN KEY (USER_ID)           REFERENCES TB_USERS(USER_ID),
  CONSTRAINT FK_ATTENDANCE_STATUS FOREIGN KEY (ATTENDANCE_STATUS) REFERENCES TB_ATTENDANCE_STATUS_CODE(ATTENDANCE_STATUS_CODE),
  CONSTRAINT UQ_ATTENDANCE_UNIQUE UNIQUE (USER_ID, ATTENDANCE_DATE)
);

CREATE INDEX IDX_ATTENDANCE_USER   ON TB_ATTENDANCE_RECORDS(USER_ID);
CREATE INDEX IDX_ATTENDANCE_DATE   ON TB_ATTENDANCE_RECORDS(ATTENDANCE_DATE);
CREATE INDEX IDX_ATTENDANCE_STATUS ON TB_ATTENDANCE_RECORDS(ATTENDANCE_STATUS);


-- 4. 뷰 (Views)
-- ========================================

-- 4-1. 사용자 연차 현황 뷰 (당해 연도 기준)
CREATE OR REPLACE VIEW VW_USER_LEAVE_SUMMARY AS
SELECT
  U.USER_ID,
  U.EMPLOYEE_NO,
  U.USER_NAME,
  U.DEPARTMENT_CODE,
  D.DEPARTMENT_NAME,
  D.GROUP_NAME,
  U.POSITION_CODE,
  U.TOTAL_LEAVE_DAYS,
  NVL(SUM(CASE WHEN LR.LEAVE_STATUS_CODE = 'APPROVED'
               THEN (LR.END_DATE - LR.START_DATE + 1)
               ELSE 0 END), 0) AS USED_LEAVE_DAYS,
  (U.TOTAL_LEAVE_DAYS - NVL(SUM(CASE WHEN LR.LEAVE_STATUS_CODE = 'APPROVED'
                                      THEN (LR.END_DATE - LR.START_DATE + 1)
                                      ELSE 0 END), 0)) AS REMAINING_LEAVE_DAYS
FROM TB_USERS U
JOIN TB_DEPARTMENT_CODE D ON U.DEPARTMENT_CODE = D.DEPARTMENT_CODE
LEFT JOIN TB_LEAVE_REQUESTS LR
  ON U.USER_ID = LR.USER_ID
  AND EXTRACT(YEAR FROM LR.START_DATE) = EXTRACT(YEAR FROM SYSDATE)
WHERE U.STATUS = 'A'
GROUP BY U.USER_ID, U.EMPLOYEE_NO, U.USER_NAME,
         U.DEPARTMENT_CODE, D.DEPARTMENT_NAME, D.GROUP_NAME,
         U.POSITION_CODE, U.TOTAL_LEAVE_DAYS;


-- 4-2. 날짜별 팀원 휴가 현황 뷰 (달력용 — 승인된 연차만)
--   각 연차 기간을 날짜별로 전개 (Oracle CONNECT BY 활용)
CREATE OR REPLACE VIEW VW_TEAM_LEAVE_BY_DATE AS
SELECT
  LR.START_DATE + LEVEL - 1     AS LEAVE_DATE,
  U.USER_ID,
  U.EMPLOYEE_NO,
  U.USER_NAME,
  U.DEPARTMENT_CODE,
  D.DEPARTMENT_NAME,
  D.GROUP_NAME
FROM TB_LEAVE_REQUESTS LR
JOIN TB_USERS U         ON LR.USER_ID        = U.USER_ID
JOIN TB_DEPARTMENT_CODE D ON U.DEPARTMENT_CODE = D.DEPARTMENT_CODE
WHERE LR.LEAVE_STATUS_CODE = 'APPROVED'
  AND U.STATUS = 'A'
CONNECT BY LEVEL <= (LR.END_DATE - LR.START_DATE + 1)
       AND PRIOR LR.LEAVE_REQUEST_ID = LR.LEAVE_REQUEST_ID
       AND PRIOR SYS_GUID() IS NOT NULL
ORDER BY LEAVE_DATE, U.USER_ID;


