-- ========================================
-- 설계 원칙 및 참고사항
-- ========================================
-- 1. 부서 접근 제어: DEPARTMENT_CODE (D001~D005) 값으로만 비교 — DEPARTMENT_NAME 문자열 비교 금지
-- 2. L4 권한: POSITION_CODE = 'L4' 인 사용자만 GET /api/leaves/pending, PATCH /api/leaves/:id/status 가능
-- 3. 연차 상태 전이: PENDING → APPROVED or REJECTED (직접 APPROVED 생성 불가)
-- 4. WORKING 상태: CHECK_IN_TIME 있고 CHECK_OUT_TIME 없는 당일 레코드 — API 응답 시 파생 처리
-- 5. Soft Delete: TB_USERS.STATUS = 'D' 로 논리 삭제 (물리 삭제 없음)
-- 6. 비밀번호: 반드시 bcrypt 또는 PBKDF2 해싱 후 저장
-- 7. 전체 50명 사용자 INSERT: workboard-backend/src/data/users.js 의 데이터를 변환하여 사용
